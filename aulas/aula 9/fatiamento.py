frase=('Curso de python')

print(frase[5])
# pega o elemento número cinco com a cequência (0,1,2,3,4,5,6,...)

print(frase[5:9])
# vai do 5 até o 9 sem contar o 9 

print(frase[5:13:2])
# vai do 5 até o 13 pulando de dois em dois (coloco um elemento, pulo pra um, pulo pra outro e considero o outro)

print(frase[:5])
# do início até o 5

print(frase[5:])
# do 5 ao fim

print(frase[2::3])
# do 2 até o final de 3 em 3